'''
练习4:
    从键盘输入小明的期末成绩:
        当成绩为100时，“奖励一辆BMW”
        当成绩为[80-99]时，“奖励一辆iPhone”
        当成绩为[60-79]时，“奖励一本参考书”
        其他时，“什么奖励都没有”
'''

a = float(input('请输入小明的成绩:'))
if a == 100:
    print('奖励一辆BMW')
elif 80 <= a <= 99:
    print('奖励一台iPhone')
elif 60 <= a <= 79:
    print('奖励一本参考书')
else:
    print('什么奖励都没有')